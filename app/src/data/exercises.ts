export const EXERCISE_MAP = Object.fromEntries(EXERCISES.map(e => [e.id, e]))

export function buildAllExercises(
  customExercises: Exercise[],
  overrides: Record<string, ExerciseOverride>,
  hiddenIds: string[]
): Exercise[] {
  const hiddenSet = new Set(hiddenIds)
  return [
    ...EXERCISES.filter(e => !hiddenSet.has(e.id)),
    ...customExercises,
  ].map(e => {
    const ov = overrides[e.id]
    return ov ? { ...e, ...ov } : e
  })
}
